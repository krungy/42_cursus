#include "libft.h"

int	strncmp(const char *s1, const char *s2, size_t len)
{
	size_t	i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i] && len > 0)
	{
		i++;
		len--;
	}
	if (len == 0)
		return (0);
	return (s1[i] - s2[i]);
}
